package ro.bcr.fundamentals._4_operators;

public class RelationalOperators {

    public static void main(String[] args) {

        int a = 10;
        int b = 15;

        System.out.println(a == b);

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);

    }
}
